package typecasting;

public class t1 {
	public static void main(String[] args) {
		
		//typecasting: converting variable of one datatype to another datatype
		// between numbers
		//syntax:   (desired datatype) variable to be converted 
		
		// lower=> high=> upcasting         eg: int=> double 
		// high=> low  => down casting      eg: double => integer
		
		int a=30;
		System.out.println(a);
		double r = (double)a;
		System.out.println(r);
		
		
		float m=3.49f;
		System.out.println(m);
		
		int g = (int)m;
		System.out.println(g);
		
		
		// 90 to 90.0
		
		// 4.56 -> 4
		
		
		
	}

}
